/* En este ejercicio de hará uso del operador de comparación ( >= ). Este operador verifica
si una variable es mayor o igual que otra. Los operadores de comparación actúan como booleanos, es decir,
puede ser 1 si es verdadero y 0 si es falso */
#include <iostream>
using namespace std;

int main() {
	int x = 7;
	int y = 3;
	cout << (x >= y); // Se utilizan paréntesis al verificar esta expresión
	
// 7 es mayor o igual que 3, por lo tanto, esto es verdadero (1)
	
	return 0;
}

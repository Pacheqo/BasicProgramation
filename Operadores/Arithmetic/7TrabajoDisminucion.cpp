/* En este ejercicio se hará uso del operador ( -- ). Al usar este operador, la variable disminuye
1 unidad */
#include <iostream>
using namespace std;

int main() {
	int x = 6;
	cout << --x << endl;
	
	int y = 12;
	cout << ----y; // Al usarse dos veces el operador, la variable disminuye 2 unidades
	
	return 0;
}

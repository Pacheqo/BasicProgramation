/* En este ejercicio se hará uso del operador de incremento ( ++ ). Este operador incrementa el valor
de una variable por 1 unidad */
#include <iostream>
using namespace std;

int main() {
	int x = 5;
	cout << ++x << endl;
	
	int y = 8;
	cout << ++++y; // Aquí se añade dos veces el operador, por lo tanto, incrementa el valor de ´y´ por 2 unidades
	
	return 0;
}


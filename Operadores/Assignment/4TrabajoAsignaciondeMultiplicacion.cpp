/* En este ejercicio se hará uso del operador Asignación de Multiplicación ( *= ). Este operador
multiplica un valor por una variable */
#include <iostream>
using namespace std;

int main() {
	int x = 10;
	x *= 5;
	cout << x;
	
	// El siguiente ejercicio es otra forma de describir al anterior
	
	int y = 10;
	y = y * 5;
	cout << "\n" << y;
	
	return 0;
}

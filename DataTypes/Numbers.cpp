/* En este ejercicio se usarán los tipos de datos numéricos: int, float y double */

#include <iostream>
using namespace std;

int main() {
	
	int entero = 15; // int define una variable de números enteros
	cout << entero << endl;
	
	float decimal1 = 20.123; // float define una variable de números racionales y es capáz de almacenar de 6-7 decimales
	cout << decimal1 << endl;
	
	double decimal2 = 55e10; // double funciona de igual forma que float. Sin embargo, almacena más del doble de decimales que float
	cout << decimal2 << endl;
	
	return 0;
}

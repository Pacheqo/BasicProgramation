/* En este ejercicio se usará el tipo de dato "booleano". Los booleanos determinan un valor como verdadero (1) o falso (0) */
#include <iostream>
using namespace std;

int main() {
	bool isCodingFun = true;
	bool isFishTasty = false;
	cout << isCodingFun << "\n";
	cout << isFishTasty << "\n";
	
	bool myBoolean1 = true, myBoolean2 = false; // Las comas funcionan para definir diferentes variables simultaneamente
	cout << myBoolean1 << endl << myBoolean2;
	
	return 0;
}
